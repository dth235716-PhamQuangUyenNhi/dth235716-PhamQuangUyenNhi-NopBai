#cau 4: Hãy cho biết kết quả của Boolean Expression
#a) x == 3 → True
#b) x < y → True
#c) x >= y → False
#d) x <= y → True
#e) x != y - 2 → y-2 = 3 nên 3 != 3 → False
#f) x < 10 → True
#g) x >= 0 and x < 10 → True (cả hai đều đúng)
#h) x < 0 and x < 10 → False (x < 0 sai)
#i) x >= 0 and x < 2 → False (x < 2 sai)
#j) x < 0 or x < 10 → True (phần x < 10 đúng)
#k) x > 0 or x < 10 → True (cả hai đều đúng, hoặc đủ 1 đúng)
#l) x < 0 or x > 10 → False